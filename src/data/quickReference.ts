/**
 * Quick Reference Data for D&D Content
 * Centralized data for tooltips and popups throughout the app
 */

// Spell Reference
export interface SpellRef {
  id: string
  name: string
  school: 'Abjuration' | 'Conjuration' | 'Divination' | 'Enchantment' | 'Evocation' | 'Illusion' | 'Necromancy' | 'Transmutation'
  level: number | 'cantrip'
  castingTime: string
  range: string
  components: string
  duration: string
  description: string
  higherLevels?: string
  classes: string[]
}

// Skill Reference
export interface SkillRef {
  id: string
  name: string
  ability: 'strength' | 'dexterity' | 'constitution' | 'intelligence' | 'wisdom' | 'charisma'
  description: string
  examples: string[]
}

// Ability Reference
export interface AbilityRef {
  id: string
  name: string
  abbreviation: string
  description: string
  skills: string[]
  commonUses: string[]
}

// Weapon Reference
export interface WeaponRef {
  id: string
  name: string
  category: 'simple' | 'martial'
  type: 'melee' | 'ranged'
  damage: string
  damageType: string
  properties: string[]
  weight: string
  cost: string
  description?: string
}

// Armor Reference
export interface ArmorRef {
  id: string
  name: string
  category: 'light' | 'medium' | 'heavy' | 'shield'
  ac: string
  strength?: number
  stealthDisadvantage: boolean
  weight: string
  cost: string
  description?: string
}

// Condition Reference
export interface ConditionRef {
  id: string
  name: string
  effects: string[]
  endCondition?: string
}

// Trait/Feature Reference
export interface TraitRef {
  id: string
  name: string
  source: string
  description: string
  mechanics?: string
}

